package com.example.jeewan.Profile;

public class ProfileModel {
    String name;
    String age;
    String contact;
    String city;
    String state;


    public ProfileModel(String name, String age, String contact, String city, String state) {
        this.name = name;
        this.age = age;
        this.contact = contact;
        this.city = city;
        this.state = state;
    }
}
